    private void cargarPeliculas (){
            
        List <Pelicula> lista = peliData.listarPeliculas();
        modelo.setRowCount(0);
        String enCarte = "";
        

        for (Pelicula p: lista) {
            
            if (p.isEnCartelera() == true) {
                
                enCarte = "Si";
            
            }   else {
                
                enCarte= "No";
            }   
                
         
            modelo.addRow(new Object []{
               p.getIdPelicula(),
               p.getTitulo(),
               p.getDirector(),
               p.getActores(),
               p.getOrigen(),
               p.getGenero(),
               p.getEstreno(),
               enCarte
            });
        }

    }
    
    
    
    
    
    
    
    
